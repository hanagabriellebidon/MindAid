      <h3>Examples of a Crisis</h3>
      <h4>Suicide</h4>
      <img src="/images/suicide.jpg" alt="Suicide" style="width:200px; height:150px;">
      <!-- Source: https://www.gannett-cdn.com/presto/2018/09/21/PNAS/42d470ec-07bc-4342-8480-e7d02a929fa7-MentalHealth_Suicide_028.jpg?crop=5567,3131,x1,y490&width=3200&height=1680&fit=bounds-->
      Source: <cite><a href="https://www.gannett-cdn.com/presto/2018/09/21/PNAS/42d470ec-07bc-4342-8480-e7d02a929fa7-MentalHealth_Suicide_028.jpg?crop=5567,3131,x1,y490&width=3200&height=1680&fit=bounds">https://www.gannett-cdn.com/presto/2018/09/21/PNAS/42d470ec-07bc-4342-8480-e7d02a929fa7-MentalHealth_Suicide_028.jpg?crop=5567,3131,x1,y490&width=3200&height=1680&fit=bounds</a></cite>
      <ul>
      <li>Talking openly about suicide lowers anxiety and enables the person to talk about their emotions.</li>
      <li>When assessing the risk of suicide, use these four steps:</li>
      <li>Intent: Does the person have the desire to kill themselves?</li>
      <li>Plan: Does the person have a solid plan to take their own life?</li>
      <li>Means: Does the person have anything to kill themselves with them near them (e.g. gun, knife, rope, etc.)?</li>
      <li>Timeframe: Does the person know when they're going to kill themselves within 24 hours?</li>
      <li>If the person says yes to all four steps of risk assessment, they're at imminent risk.</li>
      <li>Come up with a safety plan, if the person is willing to do that.</li>
      <li>If the person is unwilling to come up with a safety plan, call 911 immediately.</li>

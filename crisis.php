      <li>Misconception bunked: People who self-harm don't necessarily have a mental illness. Common motivations for self-harm are to distract themselves from emotional pain, punishing themselves for bad behavior, and a sense of europhia in the short term.</li>
      <li>Don't judge a person who self-harms because they may do it for various reasons as mentioned above.</li>
      <li>Identify healthier coping mechancisms to deal with their emotions (e.g. drawing on parts of their body that they usually self-harm, listening to music, etc.)</li>

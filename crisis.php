      <h3>What counts as a crisis?</h3>
      <p>A crisis is when someone is in a situation in which they are displaying extreme emotional or behavioral distress.</p>

      <h3>General Tips</h3>
      <img src="/images/tips.jpg" alt="Tips" style="width:300px; height:200px;">
      <!-- Source: https://mageewp.com/wp-content/uploads/2016/08/Tips.jpg-->
      Source: <cite><a href="https://mageewp.com/wp-content/uploads/2016/08/Tips.jpg">https://mageewp.com/wp-content/uploads/2016/08/Tips.jpg</a></cite>
      <ul>
          <li>Actively listen to what the person is saying instead of listening to speak.</li>
          <li>Practice self-care after you help the person in crisis.</li>
          <li>Reflect and paraphrase the person's statements to show that you understand their issues and that the person knows that you truly hear them.</li>
          <li>Validate the person's thoughts, feelings, and experiences to let the person know that their feelings are normal and natural. It can help the person open up further and build a relationship between you and that person.</li>
          <li>Take care of yourself after you've finished talking to the person in a crisis.</li>
          <li>Self-care can be as simple as listening to music, watching TV, or hanging out with friends.</li>
          <li>When you're talking to someone who is in a crisis, it's highly recommend to go through these steps.</li>
